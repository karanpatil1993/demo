package operators;

public class logical {

	public static void main(String[] args) {
		System.out.println("logical and");
		System.out.println((10>5) && (2>1));
		System.out.println((10<5) && (2>1));
		System.out.println((10>5) && (2<1));
		System.out.println("logical or");
		System.out.println((10>5) || (2>1));
		System.out.println((10<5) || (2>1));
		System.out.println((10>5) || (2<1));
		
	}

}
