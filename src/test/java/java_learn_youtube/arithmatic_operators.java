package java_learn_youtube;

public class arithmatic_operators {

	public static void main(String[] args) {
		int a=5;
		int b=11;
		int c=a+b;
		int d=a-b;
		int e=a*b;		
		int f=b/a;
		int g=b%a;
		/*
		 * System.out.println(c); System.out.println(d); System.out.println(e);
		 * System.out.println(f); System.out.println(g); System.out.println(a==b);
		 * System.out.println(a!=b); System.out.println(a>=b); System.out.println(a<=b);
		 */
		System.out.println(a<=b&&a==b);
		System.out.println(a<=b&&a!=b);
		System.out.println(a<=b||a!=b);
		System.out.println(a>=b||a==b);
	
	
	}

}
